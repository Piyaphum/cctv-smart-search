import cv2
from ultralytics import YOLO

# ==========================================
# ‚öôÔ∏è ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤ (SETTINGS)
# ==========================================
# ‡πÉ‡∏™‡πà‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡πÇ‡∏°‡πÄ‡∏î‡∏•‡∏ó‡∏µ‡πà‡∏Ñ‡∏∏‡∏ì‡πÄ‡∏ó‡∏£‡∏ô‡∏°‡∏≤
MODEL_PATH = 'best.pt' 

# ‡πÉ‡∏™‡πà‡∏ä‡∏∑‡πà‡∏≠‡πÑ‡∏ü‡∏•‡πå‡∏ß‡∏¥‡∏î‡∏µ‡πÇ‡∏≠‡∏ó‡∏µ‡πà‡∏à‡∏∞‡∏ó‡∏î‡∏™‡∏≠‡∏ö
VIDEO_PATH = 'mix.mp4'

# (‡∏Ñ‡πà‡∏≤‡∏ô‡∏µ‡πâ‡∏™‡∏π‡∏á‡∏°‡∏≤‡∏Å ‡∏à‡∏∞‡∏à‡∏±‡∏ö‡πÄ‡∏â‡∏û‡∏≤‡∏∞‡∏ä‡πá‡∏≠‡∏ï‡∏ó‡∏µ‡πà‡∏ä‡∏±‡∏î‡πÄ‡∏õ‡πä‡∏∞‡πÜ ‡πÄ‡∏ó‡πà‡∏≤‡∏ô‡∏±‡πâ‡∏ô)
CONFIDENCE = 0.85

# ==========================================
# üöÄ ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ó‡∏≥‡∏á‡∏≤‡∏ô
# ==========================================
print(f"üî• ‡∏Å‡∏≥‡∏•‡∏±‡∏á‡πÇ‡∏´‡∏•‡∏î‡πÇ‡∏°‡πÄ‡∏î‡∏•: {MODEL_PATH} (Confidence Threshold: {CONFIDENCE}) ...")
try:
    model = YOLO(MODEL_PATH)
except Exception as e:
    print("‚ùå ‡∏´‡∏≤‡πÑ‡∏ü‡∏•‡πå‡πÇ‡∏°‡πÄ‡∏î‡∏•‡πÑ‡∏°‡πà‡πÄ‡∏à‡∏≠! ‡∏≠‡∏¢‡πà‡∏≤‡∏•‡∏∑‡∏°‡πÄ‡∏≠‡∏≤‡πÑ‡∏ü‡∏•‡πå best.pt ‡∏°‡∏≤‡∏ß‡∏≤‡∏á‡πÑ‡∏ß‡πâ‡∏ó‡∏µ‡πà‡πÄ‡∏î‡∏µ‡∏¢‡∏ß‡∏Å‡∏±‡∏ô‡∏ô‡∏∞")
    exit()

cap = cv2.VideoCapture(VIDEO_PATH)

# ‡∏ï‡∏±‡πâ‡∏á‡∏Ñ‡πà‡∏≤‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡∏ß‡∏¥‡∏î‡∏µ‡πÇ‡∏≠
save_path = 'output_high_conf.avi'
width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
fps = int(cap.get(cv2.CAP_PROP_FPS))
out = cv2.VideoWriter(save_path, cv2.VideoWriter_fourcc(*'XVID'), fps, (width, height))

print("üé• ‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏£‡∏±‡∏ô‡∏£‡∏∞‡∏ö‡∏ö... ‡∏Å‡∏î 'q' ‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏≠‡∏≠‡∏Å")

while cap.isOpened():
    success, frame = cap.read()
    if not success:
        break

    # ---------------------------------------------------------
    # üß† AI ‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡∏ï‡∏£‡∏á‡∏ô‡∏µ‡πâ
    # ---------------------------------------------------------
    # conf=CONFIDENCE (0.88) ‡∏Ñ‡∏∑‡∏≠‡∏ï‡∏±‡∏ß‡∏Å‡∏£‡∏≠‡∏á‡∏Ñ‡∏ß‡∏≤‡∏°‡∏°‡∏±‡πà‡∏ô‡πÉ‡∏à
    results = model.track(frame, persist=True, conf=CONFIDENCE, verbose=False)

    # üé® ‡∏ß‡∏≤‡∏î‡∏Å‡∏£‡∏≠‡∏ö
    annotated_frame = results[0].plot()

    # ‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•
    cv2.imshow("YOLOv11 High Confidence", annotated_frame)
    out.write(annotated_frame)

    if cv2.waitKey(1) & 0xFF == ord('q'):
        break

cap.release()
out.release()
cv2.destroyAllWindows()
print(f"‚úÖ ‡πÄ‡∏™‡∏£‡πá‡∏à‡∏™‡∏¥‡πâ‡∏ô! ‡∏ö‡∏±‡∏ô‡∏ó‡∏∂‡∏Å‡πÑ‡∏ü‡∏•‡πå‡∏ú‡∏•‡∏•‡∏±‡∏û‡∏ò‡πå‡πÑ‡∏ß‡πâ‡∏ó‡∏µ‡πà: {save_path}")